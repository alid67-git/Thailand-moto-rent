function enToRu(text) {
  let out = text;
  for (const [en, ru] of EN_RU) {
    out = out.split(en).join(ru);
  }
  return out;
}

function convertEnFile(enName, ruName, exportEn, exportRu) {
  let content = fs.readFileSync(path.join(MSG, enName), "utf8");
  content = content.replace(exportEn, exportRu);
  // translate quoted strings only (simple approach: line by line)
  content = content
    .split("\n")
    .map((line) => {
      const m = line.match(/^(\s*(?:title|description|name|tips):\s*")(.*)(",?\s*)$/);
      if (m) return `${m[1]}${enToRu(m[2]).replace(/"/g, '\\"')}${m[3]}`;
      const pm = line.match(/^(\s*places:\s*\[)(.*)(\],?\s*)$/);
      if (pm) {
        const inner = pm[2].replace(/"([^"]+)"/g, (_, s) => `"${enToRu(s)}"`);
        return pm[1] + inner + pm[3];
      }
      const sm = line.match(/^(\s*stayOptions:\s*\[")(.*)("\],?\s*)$/);
      if (sm) return `${sm[1]}${enToRu(sm[2])}${sm[3]}`;
      return line;
    })
    .join("\n");
  fs.writeFileSync(path.join(MSG, ruName), content, "utf8");
  console.log("Wrote", ruName);
}

