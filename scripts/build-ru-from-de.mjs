function deToRu(text) {
  let out = text;
  for (const [de, ru] of DE_RU) {
    out = out.split(de).join(ru);
  }
  return out;
}

function convertDeFile(dePath, ruPath, exportDe, exportRu) {
  let content = read(dePath);
  content = content.replace(exportDe, exportRu);
  content = deToRu(content);
  write(ruPath, content);
}

